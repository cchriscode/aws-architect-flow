export function t(
  lang: Locale,
  phase: string,
  qId: string
): QText | undefined {
  const dict = lang === "en" ? en : ko;
  return dict[phase]?.[qId];
}

export function tq(lang: Locale, phase: string, qId: string): string {
  return t(lang, phase, qId)?.q ?? "";
}

export function thelp(lang: Locale, phase: string, qId: string): string {
  return t(lang, phase, qId)?.help ?? "";
}

export function topt(
  lang: Locale,
  phase: string,
  qId: string,
  optVal: string,
  field: "l" | "d"
): string {
  return t(lang, phase, qId)?.opts[optVal]?.[field] ?? "";
}
